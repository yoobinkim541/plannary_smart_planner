// Currently active language and listeners
let _currentLang = (typeof localStorage !== 'undefined' && localStorage.getItem('planary.lang')) || 'ko';
const _listeners = new Set();

function t(key) {
  return (I18N_STRINGS[_currentLang] && I18N_STRINGS[_currentLang][key]) || I18N_STRINGS.ko[key] || key;
}

function setLang(lang) {
  if (!I18N_STRINGS[lang]) return;
  _currentLang = lang;
  try { localStorage.setItem('planary.lang', lang); } catch (_) {}
  document.documentElement.setAttribute('lang', lang);
  _listeners.forEach(fn => fn(lang));
}

function getLang() { return _currentLang; }
function subscribe(fn) { _listeners.add(fn); return () => _listeners.delete(fn); }

